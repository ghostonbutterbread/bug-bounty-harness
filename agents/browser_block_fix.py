"""
BrowserBlockFix — curl first, headless browser fallback when WAF blocks.

Simple middleware:
  1. Try request with curl (fast, lightweight)
  2. If WAF blocks → spawn headless Chrome via Playwright
  3. Route subsequent requests through browser
  4. Auto-close browser on exit

Usage (context manager):
    with BrowserBlockFix("https://target.com", "programname") as bbf:
        resp = bbf.get("/admin")
        resp = bbf.post("/login", json={"user": "admin", "pass": "123"})

Usage (manual):
    bbf = BrowserBlockFix("https://target.com")
    resp = bbf.get("/page")
    bbf.done()

Integration in other harnesses:
    from agents.browser_block_fix import BrowserBlockFix

    with BrowserBlockFix(target, program) as bbf:
        resp = bbf.get("/endpoint")
        # WAF blocked? bbf auto-spawns browser and retries transparently.
"""

from __future__ import annotations

import html
import json
import random
import subprocess
import shlex
import time
from typing import Optional
